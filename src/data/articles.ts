export const generateRandomCase = (): {
  accusedName: string;
  articles: CrimeArticle[];
  description: string;
} => {
  const names = [
    'Иванов Иван Иванович',
    'Петров Петр Петрович',
    'Сидоров Сидор Сидорович',
    'Козлов Андрей Викторович',
    'Морозов Дмитрий Александрович',
    'Новикова Елена Сергеевна',
    'Волков Михаил Николаевич',
    'Соколов Алексей Игоревич',
    'Лебедев Константин Олегович',
    'Егоров Владимир Дмитриевич'
  ];

  const numArticles = Math.floor(Math.random() * 2) + 1;
  const selectedArticles: CrimeArticle[] = [];
  const usedIndices = new Set<number>();

  while (selectedArticles.length < numArticles) {
    const index = Math.floor(Math.random() * CRIMINAL_ARTICLES.length);
    if (!usedIndices.has(index)) {
      usedIndices.add(index);
      selectedArticles.push(CRIMINAL_ARTICLES[index]);
    }
  }

  const descriptions = [
    'По данным следствия, обвиняемый совершил противоправные действия в период с января по март 2024 года.',
    'Согласно материалам дела, преступление было совершено при отягчающих обстоятельствах.',
    'Обвиняемый не признает свою вину и настаивает на своей невиновности.',
    'В ходе следствия были собраны неопровержимые доказательства вины обвиняемого.',
    'Потерпевшая сторона требует максимального наказания для обвиняемого.',
    'Обвиняемый частично признал вину и готов к сотрудничеству со следствием.',
    'Преступление было совершено группой лиц по предварительному сговору.',
    'Обвиняемый находился в состоянии алкогольного опьянения на момент совершения преступления.'
  ];

  return {
    accusedName: names[Math.floor(Math.random() * names.length)],
    articles: selectedArticles,
    description: descriptions[Math.floor(Math.random() * descriptions.length)]
  };
};
